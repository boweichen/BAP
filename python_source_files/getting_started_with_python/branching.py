#Define variables
a = 7
b = -2

#if statement
if a > b:
    print("As expected")
print("That cannot be true")

#the if - else statement
if a > b:
    print("As expected")
else:
    print("That cannot be true")
    
#the if - elif - else statement    
if a > b:
    print("As expected")
elif a == b:
    print("Equal")
else:
    print("That cannot be true")
